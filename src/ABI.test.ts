class TestABI extends ABI {
  constructor(type: string) {
    super(type, {
      addressFromBytes: (bytes) => `0x${bytes.toHex()}`,
      bytesFromAddress: (address) => Uint8Array.fromHex(address.slice(2)),
    });
  }
}

Deno.test('encode', async (t) => {
  for (const vector of vectors) {
    await t.step(vector.type, () => {
      const abi = new TestABI(vector.type);
      const actual = abi.encode(vector.value).toHex();
      const expected = vector.bytes;
      if (actual !== expected) {
        console.error('Value:', vector.value);
        console.error('Expected:', expected);
        console.error('Got:', actual);
        throw new Error(`Failed to encode ${vector.type}`);
      }
    });
  }
});

Deno.test('decode', async (t) => {
  for (const vector of vectors) {
    await t.step(vector.type, () => {
      const abi = new TestABI(vector.type);
      const bytes = Uint8Array.fromHex(vector.bytes);
      const actual = abi.decode(bytes);
      const expected = vector.decodedValue ?? vector.value;
      if (!deepEqual(actual, expected)) {
        console.error('Bytes:', vector.bytes);
        console.error('Expected:', expected);
        console.error('Got:', actual);
        throw new Error(`Failed to decode ${vector.type}`);
      }
    });
  }
});
